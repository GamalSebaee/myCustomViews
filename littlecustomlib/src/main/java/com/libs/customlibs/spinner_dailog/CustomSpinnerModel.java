package com.libs.customlibs.spinner_dailog;

public class CustomSpinnerModel {
    private String spinnerText;
    private int spinnerIcon;
    private String spinnerImageLink;

    public String getSpinnerText() {
        return spinnerText;
    }

    public void setSpinnerText(String spinnerText) {
        this.spinnerText = spinnerText;
    }

    public int getSpinnerIcon() {
        return spinnerIcon;
    }

    public void setSpinnerIcon(int spinnerIcon) {
        this.spinnerIcon = spinnerIcon;
    }

    public String getSpinnerImageLink() {
        return spinnerImageLink;
    }

    public void setSpinnerImageLink(String spinnerImageLink) {
        this.spinnerImageLink = spinnerImageLink;
    }
}
